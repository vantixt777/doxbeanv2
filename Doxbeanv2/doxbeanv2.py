class PingWorker(QThread):
    output = pyqtSignal(str)
    error = pyqtSignal(str)
    finished = pyqtSignal()

    def __init__(self, target, count):
        super().__init__()
        self.target = target
        self.count = count
        self._is_running = True

    def run(self):
        try:
            clean_target = self.target.replace("https://", "").replace("http://", "").split("/")[0]

            if platform.system().lower() == "windows":
                command = ["ping", "-n", str(self.count), clean_target]
            else:
                command = ["ping", "-c", str(self.count), clean_target]

            process = subprocess.Popen(
                command,
                stdout=subprocess.PIPE,
                stderr=subprocess.PIPE,
                universal_newlines=True,
                encoding='utf-8',
                errors='replace'
            )

            while self._is_running:
                output_line = process.stdout.readline()
                if output_line == '' and process.poll() is not None:
                    break
                if output_line:
                    self.output.emit(output_line.strip())

            return_code = process.poll()
            if return_code != 0:
                error_output = process.stderr.read()
                if error_output:
                    self.error.emit(f"Ping failed: {error_output}")

        except Exception as e:
            self.error.emit(f"Fatal error: {str(e)}")
        finally:
            self.finished.emit()

    def stop(self):
        self._is_running = False

